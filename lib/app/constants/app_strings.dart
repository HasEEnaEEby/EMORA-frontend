  static String getTimeBasedGreeting() {
    final hour = DateTime.now().hour;
    if (hour < 12) return greetingMorning;
    if (hour < 17) return greetingAfternoon;
    if (hour < 21) return greetingEvening;
    return greetingNight;
  }

